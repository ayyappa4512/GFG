class Solution {
  public:
    vector<int> intersectionWithDuplicates(vector<int>& a, vector<int>& b) {
        // code here
        set<int> res1(a.begin(),a.end());
        set<int> res2(b.begin(),b.end());
        unordered_map<int,int> freq;
        for(int i : res1)
        {
            freq[i]++;
        }
        for(int i : res2)
        {
            freq[i]++;
        }
        vector<int> fres;
        for(auto num : freq)
        {
            if (num.second > 1) 
            {
                fres.push_back(num.first);
            }
        }
        return fres;
    }
};

